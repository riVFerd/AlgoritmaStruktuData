package minggu9.praktikum1;

public class Pakaian {
    String jenis, warna, merk, ukuran;
    double harga;

    public Pakaian(String jenis, String warna, String merk, String ukuran, double harga) {
        this.jenis = jenis;
        this.warna = warna;
        this.merk = merk;
        this.ukuran = ukuran;
        this.harga = harga;
    }
}